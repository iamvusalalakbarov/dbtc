    <p>
      DBTC stands for "Don't Break The Chain" which is a famous methodology
      created by Jerry Seinfeld. He used this methodology to improve his
      skills about comedy, but it is suitable for everyone who needs to get
      a progress on anything; it can be reading a book, doing some sport or
      famously — learning programming.
    </p>

    <p>
      The methodology is simple: You decide for your goal, get a calendar
      and everday after doing your goal put a cross on the respective day of
      calendar. Then your new goal is not to break the chain formed by
      crosses.
    </p>

    <p>
      As a developer of this project, I learned this methodology from
      Turkish youtuber Barış Özcan. He prepares videos on this concept at
      every New Year's Eve and encourages his subscribers to define New Year
      goals. I learned lots of my programming skills with the help of this
      methodology by applying it to my daily life. Then I decided to create
      a website on this topic by using the skills I learned.
    </p>

    <p>
      Nowadays, everything becomes virtual; so I thought it is also a need
      to apply this methodology not on a paper, on a web app instead. As
      foreigner students like me, it is really hard to keep those things on
      a paper and carry it with you without hurting or damaging it. That's
      the real reason behind the project idea.
    </p>
  </div>
